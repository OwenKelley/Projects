#96
SELECT AUTHOR.AU_ID, AUTHOR.AU_FNAME, AUTHOR.AU_LNAME, BOOK.BOOK_NUM, BOOK.BOOK_TITLE
FROM AUTHOR
JOIN WRITES ON WRITES.AU_ID = AUTHOR.AU_ID
JOIN BOOK ON BOOK.BOOK_NUM = WRITES.BOOK_NUM
WHERE BOOK.BOOK_SUBJECT = 'Cloud'
ORDER BY BOOK_NUM, AU_LNAME;

