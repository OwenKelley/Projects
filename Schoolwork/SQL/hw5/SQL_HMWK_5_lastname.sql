SELECT AUTHOR.AU_ID, AUTHOR.AU_LNAME, BOOK.BOOK_TITLE, CHECK_OUT_DATE, PAT_LNAME
FROM AUTHOR
JOIN WRITES ON WRITES.AU_ID = AUTHOR.AU_ID
JOIN BOOK ON BOOK.BOOK_NUM = WRITES.BOOK_NUM
JOIN PATRON ON PATRON.PAT_ID = BOOK.PAT_ID
JOIN CHECKOUT ON CHECKOUT.BOOK_NUM = BOOK.BOOK_NUM
WHERE AUTHOR.AU_LNAME = 'Bruer' AND PATRON.PAT_LNAME = 'Miles'
ORDER BY CHECKOUT.CHECK_OUT_DATE;

